package other;

public class Year {
    private int year;
    private int id;

    public Year(int year, int id) {
        this.year = year;
        this.id = id;
    }

    public int getYear() {
        return year;
    }

    public void setName(int year) {
        this.year = year;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
